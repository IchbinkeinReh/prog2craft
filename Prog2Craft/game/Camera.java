package game;

public class Camera {

	private float x = 0.0f;
	private float y = 0.0f;
	private float z = 1.0f;
	
	public void setZ(float z) {
		this.z = z;
	}
	public float getZ() {
		return z;
	}
	public void setY(float y) {
		this.y = y;
	}
	public float getY() {
		return y;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getX() {
		return x;
	}
	
}
